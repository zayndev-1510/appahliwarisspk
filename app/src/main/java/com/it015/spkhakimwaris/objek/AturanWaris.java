package com.it015.spkhakimwaris.objek;

public class AturanWaris {

    String aturan;
    String bagian;
    double kpk;

    public AturanWaris() {
    }

    public String getAturan() {
        return aturan;
    }

    public void setAturan(String aturan) {
        this.aturan = aturan;
    }

    public String getBagian() {
        return bagian;
    }

    public void setBagian(String bagian) {
        this.bagian = bagian;
    }

    public double getKpk() {
        return kpk;
    }

    public void setKpk(double kpk) {
        this.kpk = kpk;
    }
}
