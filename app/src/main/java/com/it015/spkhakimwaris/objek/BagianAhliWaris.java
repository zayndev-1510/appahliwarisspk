package com.it015.spkhakimwaris.objek;

public class BagianAhliWaris {

    String ahli_waris;
    String value;

    public BagianAhliWaris() {
    }


    public BagianAhliWaris(String ahli_waris, String value) {
        this.ahli_waris = ahli_waris;
        this.value = value;
    }

    public String getAhli_waris() {
        return ahli_waris;
    }

    public void setAhli_waris(String ahli_waris) {
        this.ahli_waris = ahli_waris;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
